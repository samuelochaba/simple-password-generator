let generateButton = document.querySelector("#gen-btn");
let passwordContainerOne = document.querySelector("#password-1");
let passwordContainerTwo = document.querySelector("#password-2");

const generatePassword = () => {
  let passwordArray = [];
  let password = "";
  for (let i = 0; i < 15; i++) {
    passwordArray.push(Math.floor(Math.random() * characters.length));
  }

  passwordArray.forEach((item) => {
    password += characters[item];
  });

  return password;
};

generateButton.addEventListener("click", () => {
  passwordContainerOne.textContent = generatePassword();
  passwordContainerTwo.textContent = generatePassword();
});
